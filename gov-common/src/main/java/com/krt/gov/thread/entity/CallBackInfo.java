package com.krt.gov.thread.entity;

import java.util.IdentityHashMap;
import java.util.Map;

/**
 * @author 郭明德
 * @description
 * @date 2019/8/9 17:07
 */
public class CallBackInfo {

    private Integer callbackLogId;
    private Integer callbackId;
    /**
     * 回调URl
     */
    private String url;
    /**
     * 应用ID
     */
    private Integer appId;
    /**
     * 调用参数
     */
    private IdentityHashMap data;
    /**
     * 调用次数
     */
    private Integer count;
    /**
     * 延时
     */
    private Long time;

    private Boolean working;

    public Integer getCallbackId() {
        return callbackId;
    }

    public void setCallbackId(Integer callbackId) {
        this.callbackId = callbackId;
    }

    public Integer getCallbackLogId() {
        return callbackLogId;
    }

    public void setCallbackLogId(Integer callbackLogId) {
        this.callbackLogId = callbackLogId;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public Integer getAppId() {
        return appId;
    }

    public void setAppId(Integer appId) {
        this.appId = appId;
    }

    public IdentityHashMap getData() {
        return data;
    }

    public void setData(IdentityHashMap data) {
        this.data = data;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public Long getTime() {
        return time;
    }

    public void setTime(Long time) {
        this.time = time;
    }

    public Boolean getWorking() {
        return working;
    }

    public void setWorking(Boolean working) {
        this.working = working;
    }
}
